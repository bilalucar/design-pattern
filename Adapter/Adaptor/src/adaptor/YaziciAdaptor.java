public class YaziciAdaptor implements Yazdirabilme {

    @Override
    public void yazdir(String[] liste) {
        MetinYazdir metinYazici = new MetinYazdir();
        for (int i = 0; i < liste.length; i++) {
            metinYazici.yazdir(liste[i]);

        }
    }
}
